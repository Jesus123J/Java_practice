    @Override
    protected void paintComponent(Graphics g) {
        Graphics2D graphics2D = (Graphics2D) g.create();
        graphics2D.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
        graphics2D.setColor(getBackground());
        int header = 10;
        AffineTransform affineTransform = new AffineTransform();
        affineTransform.translate(getWidth() - 230, 5);
        affineTransform.rotate(Math.toRadians(45));
        Path2D p = new Path2D.Double(new RoundRectangle2D.Double(0, 0, 20, 20, 5, 5), affineTransform);
        Area are = new Area(p);
        are.add(new Area(new RoundRectangle2D.Double(0, header, getWidth(), getHeight() - header, 10, 10)));
        graphics2D.fill(are);
        // Establece el grosor del borde y color negro
        graphics2D.setStroke(new BasicStroke(5f)); // Grosor del borde (ajustable)
        graphics2D.setColor(Color.BLACK); // Color del borde
        graphics2D.draw(are); // Dibuja el borde alrededor del Ã¡rea

        graphics2D.dispose();
        super.paintComponent(graphics2D);

    }
